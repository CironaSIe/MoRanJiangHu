const 组织或身份尾缀正则 = /(?:氏|家|族|门|派|宗|阁|楼|堂|宫|城|寨|帮|会|军|营|卫|司|府|院|馆|坊|铺|行|队|众|军|守卫|护卫|侍卫|弟子|同门|师兄|师弟|师姐|师妹|掌柜|管事|太监|内侍|宫女|丫鬟|小厮|车夫)$/u;
const 明显非姓名词正则 = /^(?:一边|另一边|热茶|茶盏|茶水|热茶倾|脚步声|门外|屋内|窗外|空气|灵气|晨雾|月色|剑光|刀光|众人|有人|那人|此人|对方)$/u;

export const 姓名含已知中文姓氏 = (value: unknown): boolean => {
    const name = typeof value === 'string' ? value.trim().replace(/\s+/g, '') : '';
    if (!/^[\u4e00-\u9fa5]{2,4}$/u.test(name)) return false;
    if (明显非姓名词正则.test(name)) return false;
    if (组织或身份尾缀正则.test(name)) return false;
    if (复姓列表.some((surname) => name.startsWith(surname) && name.length > surname.length)) return true;
    return 单姓集合.has(name[0]);
};
