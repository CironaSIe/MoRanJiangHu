import { describe, expect, it } from 'vitest';
import { 规范化角色物品容器映射 } from '../hooks/useGame/stateTransforms';

const 基础角色 = {
    姓名: '测试',
    性别: '男',
    年龄: 18,
    装备: {},
    金钱: {},
    物品列表: []
};

describe('玩家 BUFF 规范化', () => {
    it('清理已经超过结束时间的玩家 BUFF', () => {
        const role = 规范化角色物品容器映射({
            ...基础角色,
            玩家BUFF: [
                { 名称: '浴血奋战', 描述: '', 效果: '威慑力提升。', 结束时间: '1:01:01:13:00' },
                { 名称: '余勇未散', 描述: '', 效果: '下一次交涉更有气势。', 结束时间: '1:01:03:13:00' }
            ]
        }, { 当前时间: '1:01:02:12:00' });

        expect(role.玩家BUFF.map((buff) => buff.名称)).toEqual(['余勇未散']);
    });

    it('清理没有实际效果的玩家 BUFF', () => {
        const role = 规范化角色物品容器映射({
            ...基础角色,
            玩家BUFF: [
                { 名称: '心如止水', 描述: '持续至本次议事结束', 效果: '', 结束时间: '持续至本次议事结束' },
                { 名称: '凝神', 描述: '', 效果: '悟性临时提升。', 结束时间: '1:01:03:12:00' }
            ]
        }, { 当前时间: '1:01:02:12:00' });

        expect(role.玩家BUFF.map((buff) => buff.名称)).toEqual(['凝神']);
    });

    it('清理正文已经结束的事件型玩家 BUFF', () => {
        const role = 规范化角色物品容器映射({
            ...基础角色,
            玩家BUFF: [
                { 名称: '议事专注', 描述: '', 效果: '议事判断更冷静。', 结束时间: '持续至本次议事结束' }
            ]
        }, {
            当前时间: '1:01:02:12:00',
            事件文本: '议事已经结束，众人散会离开大殿。'
        });

        expect(role.玩家BUFF).toEqual([]);
    });
});
